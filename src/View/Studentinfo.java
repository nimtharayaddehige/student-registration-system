    public ArrayList<SStudentinfo> getStudentinfoList()
    {
        ArrayList<SStudentinfo> studentinfo = new ArrayList<SStudentinfo>();
        Connection con = DBConnection.connectDB();
        String query = "select * from studentinfo";
        Statement st;
        ResultSet rs;
        
        try
        {
            st = con.createStatement();
            rs =st.executeQuery(query);
            SStudentinfo studinfo;
            
            while(rs.next())
            {
                studinfo = new SStudentinfo(rs.getString("studid"),rs.getString("fname"),rs.getString("surname"),
                        rs.getString("dob"),rs.getString("gender"),rs.getString("email"),rs.getString("contact"),rs.getString("address"));
                studentinfo.add(studinfo);
            }
                
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
        return studentinfo;
    }
    
