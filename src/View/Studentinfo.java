    private static String generateNextId(String lastId) {
        int nextNumber = 1;
        if (lastId != null && lastId.startsWith("S")) {
            int lastNumber = Integer.parseInt(lastId.substring(1));
            nextNumber = lastNumber + 1;
        }
        
        String formattedNumber = String.format("%03d" , nextNumber);
        return "S" + formattedNumber;
    }
    
    private String generateLastID()
    {
        String lastId = null;
        try
        {
            ResultSet resultSet;
            CStudentinfo c2 = new CStudentinfo();
            resultSet = c2.getStudentInfo();
            
            if (resultSet.next()) {
                lastId = resultSet.getString(1);
            }

        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        return lastId;
    }
    
        
        
    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        String studentid = generateNextId(generateLastID());
        String fname = txt_fname.getText();
        String surname = txt_surname.getText();
        String dob = txt_dob.getText();
        String gender = cmb_gender.getSelectedItem().toString();
        String email = txt_email.getText();
        String phn = txt_contact.getText();
        String address = txt_address.getText();
        String EMAIL_REGEX =
            "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
            
        
      if(!email.matches(EMAIL_REGEX))
      {
          JOptionPane.showMessageDialog(null, "Please enter a valid email !","INFO",JOptionPane.INFORMATION_MESSAGE);
      }
      
      else
        {
            if(studentid.isEmpty() || fname.isEmpty() || surname.isEmpty() 
                || dob.isEmpty() || email.isEmpty() || phn.isEmpty() || address.isEmpty())
              {
            JOptionPane.showMessageDialog(null, "Fill all Fields");
              }
            else
              {
            CStudentinfo stud = new CStudentinfo();
            stud.addStudent(studentid, fname, surname, dob, gender, email, phn, address);
            
            DefaultTableModel model = (DefaultTableModel)tableDark1.getModel();
            model.setRowCount(0);
            show_Jtable();  
               }
        }
        
        

        

    }//GEN-LAST:event_btn_addActionPerformed

