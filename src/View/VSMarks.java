    private void populateTable(String StudentID) {
        DefaultTableModel model = new DefaultTableModel();
        tbl_1.setModel(model);

        // Add columns to the model
        model.addColumn("Subject Name");
        model.addColumn("Marks");
        model.addColumn("Grade");
        
         ResultSet rt = null;
        // Execute the SQL query
        try {
             Statement st = Model.DBConnection.connectDB().createStatement();
             rt  = st.executeQuery("SELECT m.* from marks m WHERE m.StudentID='"+StudentID+"'");
             

            // Iterate through the result set and add rows to the model
            while (rt.next()) {
                Object[] row = {
                        rt.getString("SubName"),
                        rt.getDouble("Marks"),
                        rt.getString("Grade")
                };
                //System.out.println("hai");
                model.addRow(row);
            }
            //System.out.println(model.getRowCount());
            //System.out.println("hello");
        } catch (SQLException e) {
            e.printStackTrace();
            // Handle any exceptions or show an error message
        }
        catch (Exception e) {
            e.printStackTrace();
            // Handle any exceptions or show an error message
        }
         tbl_1.setModel(model);
      
         // Create a font with the desired size
    Font tableFont = new Font("Segoe UI", Font.PLAIN, 36); // Change the font and size as needed

    // Create a cell renderer with the new font
    DefaultTableCellRenderer cellRenderer = new DefaultTableCellRenderer();
    cellRenderer.setFont(tableFont);

    // Apply the cell renderer to all columns
    for (int columnIndex = 0; columnIndex < tableMode2.getColumnCount(); columnIndex++) {
        tbl_1.getColumnModel().getColumn(columnIndex).setCellRenderer(cellRenderer);
    }
    }
    
   
