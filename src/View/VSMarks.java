     private void populateStudentID() {
        // Establish a database connection
        ResultSet rt = null;
        try
        {
            Statement st = Model.DBConnection.connectDB().createStatement();
            rt =  st.executeQuery("SELECT s.studid FROM studentinfo s;");
            
            ArrayList<String> studentID = new ArrayList<>();
            while (rt.next()) {
                studentID.add(rt.getString("studid"));
            }
            
            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(studentID.toArray(new String[0]));
            cb_Id.setModel(model);
            
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }
   
